-- Time to start recording
function M.TimeBegin(name)
    M.CurTimeIndex = M.CurTimeIndex + 1;
    M.TimeNames[M.CurTimeIndex] = name or tostring(M.CurTimeIndex);
    M.TimeCounts[M.CurTimeIndex] = -os.clock();
end
-- Time to end the record
function M.TimeEnd()
    M.TimeCounts[M.CurTimeIndex] = os.clock() + M.TimeCounts[M.CurTimeIndex];
end

-- Start recording memory overhead
function M.GCBegin(name)
    M.CurAllocIndex = M.CurAllocIndex + 1;
    M.GCAllocNames[M.CurAllocIndex] = name or tostring(M.CurAllocIndex);
    M.GCAllocCounts[M.CurAllocIndex] = 0;
    M.GCAllocCountsCollect[M.CurAllocIndex] = 0;
    collectgarbage("collect");
    local _count = collectgarbage("count");
    collectgarbage("stop")
    M.GCAllocCounts[M.CurAllocIndex] = -_count
    M.GCAllocCountsCollect[M.CurAllocIndex] = -_count
end

-- End record memory overhead
function M.GCEnd()
    local _count = collectgarbage("count");
    M.GCAllocCounts[M.CurAllocIndex] = (_count + M.GCAllocCounts[M.CurAllocIndex]) * 1024;
    collectgarbage("collect");
    _count = collectgarbage("count");
    M.GCAllocCountsCollect[M.CurAllocIndex] = (_count + M.GCAllocCountsCollect[M.CurAllocIndex]) * 1024;
    collectgarbage("restart");
end

function M.Print()
    for i=1, M.CurTimeIndex do
        print("name = ",M.TimeNames[i],", use time = ",M.TimeCounts[i])
    end
    for i=1, M.CurAllocIndex do
        print("name = ",M.GCAllocNames[i],",no collect gc = ",M.GCAllocCounts[i],",collect gc",M.GCAllocCountsCollect[i])
    end
end

